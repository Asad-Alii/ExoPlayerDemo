import com.google.android.exoplayer2.util.Util;

public class MainActivity extends AppCompatActivity implements AdsMediaSource.MediaSourceFactory {

    PlayerView playerView;
    ProgressBar video_spinner;

    String videoUri = "https://www.radiantmediaplayer.com/media/bbb-360p.mp4";

    private DataSource.Factory dataSourceFactory;

    private SimpleExoPlayer player;
    private long contentPosition;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        playerView = findViewById(R.id.exo_player);
        video_spinner = findViewById(R.id.video_spinner);

        initializePlayer(playerView);
    }

    public void initializePlayer(PlayerView playerView) {
        dataSourceFactory = new DefaultDataSourceFactory(this, Util.getUserAgent(this, ""));

        player = ExoPlayerFactory.newSimpleInstance(this);
        playerView.setPlayer(player);
        playerView.setUseController(true);

        play(videoUri);
    }

    public void play(String contentUrl) {
        if (player != null) {

            MediaSource contentMediaSource = buildMediaSource(Uri.parse(contentUrl));

            player.seekTo(contentPosition);
            player.prepare(contentMediaSource);
            player.setPlayWhenReady(true);
        }
    }

    @Override
    public MediaSource createMediaSource(Uri uri) {
        return buildMediaSource(uri);
    }

    @Override
    public int[] getSupportedTypes() {
        // IMA does not support Smooth Streaming ads.
        return new int[] {C.TYPE_DASH, C.TYPE_SS, C.TYPE_HLS, C.TYPE_OTHER};
    }

    private MediaSource buildMediaSource(Uri uri) {
        @C.ContentType int type = Util.inferContentType(uri);
        switch (type) {
            case C.TYPE_DASH:
                return new DashMediaSource.Factory(dataSourceFactory).createMediaSource(uri);
            case C.TYPE_SS:
                return new SsMediaSource.Factory(dataSourceFactory).createMediaSource(uri);
            case C.TYPE_HLS:
                return new HlsMediaSource.Factory(dataSourceFactory).createMediaSource(uri);
            case C.TYPE_OTHER:
                return new ProgressiveMediaSource.Factory(dataSourceFactory).createMediaSource(uri);
            default:
                throw new IllegalStateException("Unsupported type: " + type);
        }
    }


    private void releasePlayer() {
        if (player != null) {
            player.release();
            player = null;
        }
    }

    private void pausePlayer() {
        if (player != null) {
            player.setPlayWhenReady(false);
            player.getPlaybackState();
        }
    }

    private void resumePlayer() {
        if (player != null) {
            player.setPlayWhenReady(true);
            player.getPlaybackState();
        }
    }

    @Override
    protected void onPause() {
        super.onPause();
        pausePlayer();
    }

    @Override
    protected void onRestart() {
        super.onRestart();
        resumePlayer();
    }

    @Override
    protected void onDestroy() {
        super.onDestroy();
        releasePlayer();
    }
}
